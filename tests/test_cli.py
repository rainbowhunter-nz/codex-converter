def test_generated_config_toml_keeps_lines_under_120_characters(tmp_path: Path) -> None:
    values = ", ".join(f'"item-{index:02d}"' for index in range(20))
    write(tmp_path / ".claude" / "settings.json", f'{{"permissions": {{"allow": [{values}]}}}}')

    result = runner.invoke(app, ["convert", str(tmp_path)])

    assert result.exit_code == 0
