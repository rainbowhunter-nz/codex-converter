def write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)


def make_fixture(root: Path) -> None:
    write(root / "CLAUDE.md", "Root instructions\n")
    write(root / "pkg" / "CLAUDE.md", "Nested instructions\n")
    write(root / ".claude" / "skills" / "review" / "SKILL.md", "# Review\n")
    write(root / ".claude" / "skills" / "review" / "references" / "notes.md", "notes\n")
    write(root / ".claude" / "commands" / "ship.md", "Ship it\n")
    write(
        root / ".claude" / "agents" / "planner.md",
        "---\nname: planner\ndescription: Plans small tasks.\n---\nPlan carefully.\n",
    )
    write(root / ".claude" / "settings.json", '{"hooks": {}, "permissions": {"allow": []}}')
    write(root / ".claude" / "settings.local.json", "{}")


