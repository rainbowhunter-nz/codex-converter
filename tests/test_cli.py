def test_convert_creates_expected_tree(tmp_path: Path) -> None:
    make_fixture(tmp_path)

    result = runner.invoke(app, ["convert", str(tmp_path)])

    assert result.exit_code == 0
    assert (tmp_path / "AGENTS.md").read_text().startswith(
        "<!-- Generated by codex-converter from CLAUDE.md -->"
    )
    assert (tmp_path / "pkg" / "AGENTS.md").exists()
    skill = tmp_path / ".agents" / "skills" / "review"
    assert "name: review" in (skill / "SKILL.md").read_text()
    assert "description: Converted Claude skill review." in (skill / "SKILL.md").read_text()
    assert (skill / "references" / "notes.md").read_text() == "notes\n"
    assert "<!-- Generated by codex-converter from .claude/skills/review/SKILL.md -->" in (
        skill / "SKILL.md"
    ).read_text()
    command_skill = (tmp_path / ".agents" / "skills" / "ship" / "SKILL.md").read_text()
    assert "name: ship" in command_skill
    assert "<!-- Generated by codex-converter from .claude/commands/ship.md -->" in command_skill
    agent = tomllib.loads((tmp_path / ".codex" / "agents" / "planner.toml").read_text())
    assert agent["name"] == "planner"
    assert agent["description"] == "Plans small tasks."
    assert agent["developer_instructions"] == "Plan carefully.\n"
    tomllib.loads((tmp_path / ".codex" / "config.toml").read_text())


def test_default_conversion_skips_local_settings(tmp_path: Path) -> None:
    write(tmp_path / ".claude" / "settings.json", '{"permissions": {"allow": ["base"]}}')
    write(tmp_path / ".claude" / "settings.local.json", '{"permissions": {"allow": ["local"]}}')

    result = runner.invoke(app, ["convert", str(tmp_path)])

    assert result.exit_code == 0
    assert "settings.local" in result.output
    assert "skipped" in result.output
    config = tomllib.loads((tmp_path / ".codex" / "config.toml").read_text())
    assert config["codex_converter"]["claude_settings"]["permissions"]["allow"] == ["base"]


def test_include_local_settings_merges_recursively(tmp_path: Path) -> None:
    write(
        tmp_path / ".claude" / "settings.json",
        '{"permissions": {"allow": ["base"], "deny": ["keep"]}, "scalar": "base"}',
    )
    write(
        tmp_path / ".claude" / "settings.local.json",
        '{"permissions": {"allow": ["local"]}, "scalar": false, "localOnly": 3}',
    )

    result = runner.invoke(app, ["convert", str(tmp_path), "--include-local-settings"])

    assert result.exit_code == 0
    config = tomllib.loads((tmp_path / ".codex" / "config.toml").read_text())
    settings = config["codex_converter"]["claude_settings"]
    assert settings["permissions"]["allow"] == ["local"]
    assert settings["permissions"]["deny"] == ["keep"]
    assert settings["scalar"] is False
    assert settings["localOnly"] == 3


